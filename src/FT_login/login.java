    private void button_loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_loginMouseClicked
        String username = log_username.getText().trim();
        char[] pass = log_pass.getPassword();
        String password = new String(pass);
        if (!username.isEmpty()) {
            try {
                authenticate(username, password);
            } catch (AuthenticationException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Information", JOptionPane.INFORMATION_MESSAGE);
                return;
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak boleh kosong","Information",JOptionPane.INFORMATION_MESSAGE);
        }
        this.setVisible(false);
    }//GEN-LAST:event_button_loginMouseClicked

