    public static boolean authenticate(String username, String password) throws Exception {
        Connection conn = null;
        try {
            if (isAdmin(username, password, conn)) {
                admin_frame();
                return true;
            } else if (isUser(username, password, conn)) {
                user_frame();
                return true;
            } else {
                throw new AuthenticationException("Username atau Password salah.\nJika belum punya akun, silahkan klick register di bawah ya.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            if (conn != null) {
                try {
                    conn.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }
        return false;
    }
    
    public static boolean isAdmin(String username, String pass, Connection connection) throws SQLException {
        String sql = "SELECT * FROM admin_reek WHERE username = ? AND password = ?";
        try (PreparedStatement ps = Conection.conection().prepareStatement(sql)) {
            ps.setString(1, username);
            ps.setString(2, pass);
            try (ResultSet rs = ps.executeQuery()) {
                return  rs.next();
            }
        }
    }
    
    public static boolean isUser(String username, String pass, Connection connection) throws SQLException {
        String sql = "SELECT * FROM user_reek WHERE username = ? AND password = ?";
        try (PreparedStatement ps = Conection.conection().prepareStatement(sql)) {
            ps.setString(1, username);
            ps.setString(2, pass);
            try (ResultSet rs = ps.executeQuery()) {
                return rs.next();
            }
        }
    }
    
